package com.operar;

public class Operando {
	
	public double Sumar(double unm1 , double num2){
		
		
		
		return unm1 + num2; 
		
	}
	
	
	public double Multiplicar(double unm1 , double num2){
		
		
		
		return unm1 * num2;
		
	}
	
	
	public double Restar(double unm1 , double num2){
		
		
		
		return unm1 - num2;
		
	}
	
	
	public double Dividir(double unm1 , double num2){
		
		
		
		return unm1 / num2; 
		
	}
	
	public double Dividir2(double unm1 , double num2){
		
		
		
		return unm1 / num2; 
		
	}




}
